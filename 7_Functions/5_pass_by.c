//Formal Parameters vs Actual Parameters
/* 
🙏Formal Parameters: These are the parameters defined in the function
                    declaration/definition.
                    Ex. void func(int a, int b) {
                            // a and b are formal parameters
                        }
🙏Actual Parameters: These are the parameters passed to the function
                    during the function call.
                    Ex. func(5, 10); // 5 and 10 are actual parameters 
                    Ex.


*/