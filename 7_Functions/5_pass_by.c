//Pass by value and Pass by reference
/*  
🙏Pass by value: In pass by value, a copy of the actual parameter's value
                is passed to the function.
                Ex. void func(int a) {
                         a = a + 10;
                     }
🙏pass by reference: In pass by reference, the address of the actual parameter
                    is passed to the function.
                    Ex. void func(int *a) {
                            *a = *a + 10;
                        }
*/
